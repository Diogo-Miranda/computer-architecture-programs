# G = 10^9
# M = 10^6
# K = 10^3

def giga_to_mega(value):
    return ((value) * 10**9) / 10**6

def giga_to_kilo(value):
    return ((value) * 10**9) / 10**3

def mega_to_giga(value):
    return ((value) * 10**6) / 10**9

def mega_to_kilo(value):
    return ((value) * 10**6) / 10**3

def kilo_to_mega(value):
    return ((value) * 10**3) / 10**6

def kilo_to_giga(value):
    return ((value) * 10**3) / 10**9